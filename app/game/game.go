const (
	// coordinatesPrealloc hold default value for slice size preallocation.
	coordinatesPrealloc = 625
)

// Simulator represents Conway's Game of Life simulation.
type Simulator interface {
	// Simulate takes a context.Context for cancellation
	// and first Generation of cells and runs the simulations.
	Simulate(ctx context.Context, gen Generation) error
	// SimulateTick takes a context.Context for cancellation
	// and first Generation of cells and simulates on tick.
	// Returns next Generation or error.
	SimulateTick(ctx context.Context, gen Generation) (Generation, error)
}

// NewGeneration returns new Generation. All cells are
// dead by default. NewGeneration takes variadic liveCells
// which set live cells to the new Generation.
func NewGeneration(liveCells ...Coordinate) Generation {
	var gen Generation

	for _, c := range liveCells {
		gen[c.Y][c.X] = byte(1)
	}

	return gen
}

// Coordinate represents the coordinate of one cell.
type Coordinate struct{ X, Y int }

// Generation represents one generation of cells (25x25)
// of Conway's Game of Life.
type Generation [25][25]byte

// NextGeneration returns new Generation based on previous.
func (g Generation) NextGeneration() Generation {
	liveCells := make([]Coordinate, 0, coordinatesPrealloc)

	for y := 0; y < len(g); y++ {
		for x := 0; x < len(g[0]); x++ {
			if g.nextCellState(x, y) == byte(1) {
				liveCells = append(liveCells, Coordinate{X: x, Y: y})
			}
		}
	}

	return NewGeneration(liveCells...)
}

// isCellAlive reports whether the specified cell is alive or dead.
func (g Generation) isCellAlive(x, y int) byte {
	x += len(g[0])
	x %= len(g[0])
	y += len(g)
	y %= len(g)
	return g[y][x]
}

// nextCellState returns cell state for next generation.
func (g Generation) nextCellState(x, y int) byte {
	alive := 0
	for i := -1; i <= 1; i++ {
		for j := -1; j <= 1; j++ {
			if (j != 0 || i != 0) && g.isCellAlive(x+i, y+j) == byte(1) {
				alive++
			}
		}
	}

	if alive == 3 || alive == 2 && g.isCellAlive(x, y) == byte(1) {
		return byte(1)
	}

	return byte(0)
}
